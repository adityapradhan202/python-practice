# Parameters of print function
# 1. sep -> Seperator
# 2. end -> how the string inside print will end

print("Python", "Java") # Default value of sep is " "
print("Python", "Java", sep="--")

print("Hello world") # by default end="\n" (newline)
print("Second line -> New Line")

# print() # Empty line
print("Realme phone is good!", end=" 999") # space is also included with 999